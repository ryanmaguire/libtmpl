/*  Function prototype given here.                                            */
#include <libtmpl/include/tmpl_integer.h>

/*  Function for finding the minimum of two unsigned shorts.                  */
unsigned short int tmpl_UShort_Min(unsigned short int x, unsigned short int y)
{
    if (x < y)
        return x;
    else
        return y;
}
/*  End of tmpl_UShort_Min.                                                   */
