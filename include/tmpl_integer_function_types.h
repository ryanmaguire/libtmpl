#ifndef TMPL_INTEGER_FUNCTION_TYPES_H
#define TMPL_INTEGER_FUNCTION_TYPES_H

typedef signed char (*tmpl_CharFunction)(signed char);
typedef signed short int (*tmpl_ShortFunction)(signed short int);
typedef signed int (*tmpl_IntFunction)(signed int);
typedef signed long int (*tmpl_LongFunction)(signed long int);

typedef unsigned char (*tmpl_UCharFunction)(unsigned char);
typedef unsigned short int (*tmpl_UShortFunction)(unsigned short int);
typedef unsigned int (*tmpl_UIntFunction)(unsigned int);
typedef unsigned long int (*tmpl_ULongFunction)(unsigned long int);

#endif
